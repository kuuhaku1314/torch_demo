def train_epoch_ch8(net, train_iter, loss, updater, device, use_random_iter):
    """训练网络一个迭代周期（定义见第8章）"""
    state, timer = None, Timer()
    metric = Accumulator(2)  # 训练损失之和,词元数量
    # X=(32, 35), Y=(32, 35)
    for X, Y in train_iter:
        if state is None or use_random_iter:
            # 在第一次迭代或使用随机抽样时初始化state，因为若用随机抽样，则每批次序列之间是没有关联的，
            # 不能使用上一批序列学习到的隐状态去推断下一批的状态
            state = net.begin_state(batch_size=X.shape[0], device=device)
        else:
            # 注意，state参数没有传入updater里，无法通过updater.zero_grad()方法清空梯度信息，所以需要在这里手动清空
            # 调用backward()函数之前都要将梯度清零，因为如果梯度不清零，
            # pytorch中会将上次计算的梯度和本次计算的梯度累加，那么梯度就不准了
            if isinstance(net, nn.Module) and not isinstance(state, tuple):
                # state对于nn.GRU是个张量
                state.detach_()
            else:
                # state对于nn.LSTM或对于我们从零开始实现的模型是个元组
                for s in state:
                    s.detach_()
        # Y.T = (35X32) y = (1120), 注意，这里的y的排序是(32个时间步0, 32个时间步1... 32个时间步34)
        y = Y.T.reshape(-1)
        X, y = X.to(device), y.to(device)
        # y_hat = (1120, 28), X = (32, 512)，注意，这里y_hat的列排序也是(32个时间步0, 32个时间步1... 32个时间步34)
        y_hat, state = net(X, state)
        # 用交叉熵计算损失，因为交叉熵在某种程度上与信息熵/(负对数似然估计)是等价的，而信息熵越大系统越混乱，推断正确的概率就越低
        # 因此这里要尽可能降低交叉熵
        l = loss(y_hat, y.long()).mean()
        if isinstance(updater, torch.optim.Optimizer):
            updater.zero_grad()
            l.backward()
            grad_clipping(net, 1)
            updater.step()
        else:
            l.backward()
            grad_clipping(net, 1)
            # 因为已经调用了mean函数，batch_size设置为1，另外自定义的updater里会把梯度清0，因此这里调用zero_grad
            updater(batch_size=1)
        metric.add(l * y.numel(), y.numel())
    return math.exp(metric[0] / metric[1]), metric[1] / timer.stop()


